package com.example.qiplatform_practice1;

public class ResultUsn {
    private int result;
    private int usn;

    public ResultUsn(int result, int usn) {
        this.result = result;
        this.usn = usn;
    }

    public int getResult() {
        return result;
    }

    public int getUsn() {
        return usn;
    }
}